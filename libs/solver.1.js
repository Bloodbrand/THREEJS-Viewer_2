 Solver = function () {
    //BEGIN PUBLIC API 
    this.addFinishLoadHandler = function (handler) {
        protectedMembers.onFinishLoadCallbacks.push(handler);
    };
    this.addModelChangedHandler = function (handler) {
        protectedMembers.onModelChangedCallbacks.push(handler);
    };
    this.modelAsXml = function () {
      return json2xml(protectedMembers.jsonObjCopy);
    };
    //Get a copy of model
    this.modelAsJson = function () {
        return protectedMembers.jsonObjCopy;
    };
    this.modifyNode = function(id, x, y, z){
        var nodes = protectedMembers.jsonObj.beamRequest.model.nodes.node;      
        for(i=0; i < nodes.length; i++){
           if(nodes[i]['@id'] == id)
                node = nodes[i]; 
        }
        if(node){
            node['@x'] = x, node['@y']=y, node['@z']=z;
            protectedMembers.jsonObjCopy.beamRequest.model = jQuery.extend({}, protectedMembers.jsonObj.beamRequest.model);
            if (protectedMembers.onModelChangedCallbacks)
                for (i = 0; i < protectedMembers.onModelChangedCallbacks.length; i++)
                    protectedMembers.onModelChangedCallbacks[i](this.model);
        }
    };  
    this.loadXml = function (filename) {
        var xmlhttp=new XMLHttpRequest();
	xmlhttp.open("GET", filename,false);
	xmlhttp.send();
	protectedMembers.xmlDoc=xmlhttp.responseXML; 
        var j = xml2json(protectedMembers.xmlDoc, "");
	protectedMembers.jsonObj = JSON.parse(j);
        protectedMembers.jsonObjCopy = jQuery.extend({}, protectedMembers.jsonObj);
        // Deep copy
        //var jsonObjCopy = jQuery.extend(true, {}, this.jsonObj);
        if (protectedMembers.onFinishLoadCallbacks)
            for (i = 0; i < protectedMembers.onFinishLoadCallbacks.length; i++)
                protectedMembers.onFinishLoadCallbacks[i](protectedMembers.model);
    };
    //END PUBLIC API
    
    
    //Protected 
    var protectedMembers = {
        model: {},
        xmlDoc: "",
        jsonObj: {},
        jsonObjCopy: {},
        needUpdate: false,
        onFinishLoadCallbacks: new Array(),
        onModelChangedCallbacks: new Array(),
        initAfterXmlLoad: function () {
        }
    };
    protectedMembers.model = this;
    protectedMembers.initAfterXmlLoad = function (msg) {
        this.xmlDoc = $.parseXML(msg);
        var j = xml2json(this.xmlDoc, "");
        this.jsonObj = JSON.parse(j);
        this.jsonObjCopy = jQuery.extend({}, this.jsonObj);
        // Deep copy
        //var jsonObjCopy = jQuery.extend(true, {}, this.jsonObj);
        if (this.onFinishLoadCallbacks)
            for (i = 0; i < this.onFinishLoadCallbacks.length; i++)
                this.onFinishLoadCallbacks[i](this.model);
    }
};
